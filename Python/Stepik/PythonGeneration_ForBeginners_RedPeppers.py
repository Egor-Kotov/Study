from string import *


def caesar_cipher(encrypt_decrypt, language, shift, word):
    eng_l = ascii_lowercase * 2
    eng_u = ascii_uppercase * 2
    rus_l = "".join(chr(i) for i in range(ord("–∞"), ord("–∞") + 32)) * 2
    rus_u = "".join(chr(i) for i in range(ord("–ê"), ord("–ê") + 32)) * 2

    if language.lower() in ("r", "rus", "—Ä", "—Ä—É—Å—Å–∫–∏–π"):
        language_l, language_u = rus_l, rus_u
    else:
        language_l, language_u = eng_l, eng_u

    if encrypt_decrypt.lower() in ('decrypt', 'd', '–¥–µ—à–∏—Ñ—Ä–æ–≤–∞—Ç—å', '–¥'):
        shift = -shift

    result = ""

    for char in word:

        if char.isalpha() and char.isupper():
            i = language_u.find(char) + shift
            result = "%s%s" % (result, language_u[i])

        elif char.isalpha():
            i = language_l.find(char) + shift
            result = "%s%s" % (result, language_l[i])

        else:
            result = "%s%s" % (result, char)

    return result


x = input().split()

for w in x:
    counter = 0
    for c in w:
        if c.isalpha():
            counter += 1
    print(caesar_cipher("—à", "e", counter, w), end=" ")


