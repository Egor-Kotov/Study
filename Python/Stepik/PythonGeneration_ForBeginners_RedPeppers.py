def is_password_good(p):

    if len(p) < 8:
        return False

    flag_u = flag_l = flag_d = False

    for ch in p:
        if ch.isupper():
            flag_u = True
        elif ch.islower():
            flag_l = True
        elif ch.isdigit():
            flag_d = True

    return flag_u and flag_l and flag_d

